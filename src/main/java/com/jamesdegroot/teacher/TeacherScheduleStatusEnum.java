package com.jamesdegroot.teacher;

/**
 * Enum representing possible teaching load statuses.
 * Values represent fractions of a full teaching load.
 */
public enum TeacherScheduleStatusEnum {
    OVER_FULL_TIME,  // >6/6
    FULL_TIME,       // 6/6
    FIVE_SIXTHS,     // 5/6
    FOUR_SIXTHS,     // 4/6
    THREE_SIXTHS,    // 3/6
    TWO_SIXTHS,      // 2/6
    ONE_SIXTH,       // 1/6
    NO_LOAD          // 0/6
} 

